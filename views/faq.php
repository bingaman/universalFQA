								<a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseAFQA">
									What is Adjusted FQI?
								</a>
							</div>
							<div id="collapseAFQA" class="accordion-body collapse">
								<div class="accordion-inner">
									This is a variation of FQI that reduces the effect of species richness on FQI and has been found useful when comparing floristic quality of sites that vary in degradation or stressors. Please see:<br>Miller, S.J., and D.H. Wardrop. 2006. <em>Adapting the Floristic Quality Assessment Index to Indicate Anthropogenic Disturbance in Central Pennsylvania Wetlands.</em> Ecological Indicators 6 (2) (April): 313–326. See also <a class="accordion-toggle" data-toggle="collapse" href="#collapseMEAN">What is Mean C and CoverWeighted Mean C</a>, in which species richness is removed entirely as part of the metric.
								</div>
							</div>
						</div>
						<div class="accordion-group">
							<div class="accordion-heading">
								<a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseMEAN">
									What is Mean C and CoverWeighted Mean C?
								</a>
							</div>
							<div id="collapseMEAN" class="accordion-body collapse">
								<div class="accordion-inner">
									These are metrics that calculate the mean value of the Coefficient of Conservatism for all species in a site, transect or plot, but excludes species richness, because of its potential confounding effects on assessing changes in floristic quality of sites that vary in degradation or stressors. Mean C is based on presence/absence; CoverWeighted Mean C is based on species cover. See Also “What is adjusted FQI?,” in which the effect of species richness is reduced, but not removed.Please see: Cohen, M.J., S. Carstenn, and C.R. Lane. 2004. Floristic quality indices for the assessment of depressional marsh condition in Florida. Ecological Applications, 14(3): 784-794.
								</div>
							</div>
