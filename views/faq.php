								<a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapse30">
									How do I record and calculate floristic quality metrics if I have plot data that include both subplot/quadrat data and additional species in the plot but not in the quadrats?
								</a>
							</div>
							<div id="collapse30" class="accordion-body collapse">
								<div class="accordion-inner">
									When using the Plot method with subplot/quadrats, record addition species outside the quadrats but inside the plot as if they are part of an additional quadrat (it’s helpful to give the “quadrat” a distinct name like “QOutside” or”RestofPlot”).  When calculating total plot floristics, keep that “quadrat” checked, but when calculating quadrat averages within the plot, uncheck the “quadrat.”   On the importance of including species outside the quadrats but in the plot or along the transect, when estimating species richness, please see: Stohlgren, T.J., K.A. Bull, and Y. Otsuki.  1998.  Comparison of rangeland vegetation sampling techniques in the Central Grasslands.  Journal of Range Management 51:164-172. 
								</div>
