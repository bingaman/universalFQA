								<a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapse20">
									What is difference between Transect and Plot Data?
								</a>
							</div>
							<div id="collapse20" class="accordion-body collapse">
								<div class="accordion-inner">
									Although there are many ways to collect quantitative floristic data, these two options cover the primary options (see figure below).
									<img src="/assets/images/transect-vs-plot.png" alt="Transect vs. Plot" height="521" width="312">
									<ol type="A">
                    <li>For Transect data, species are recorded in multiple subplots/quadrats along a transect. Typically species outside the quadrats are not listed. There is no total area for the transect, just an area per quadrat.</li>
										<br/>
                    <li>For Plot data, species are either recorded in the entire plot, with no subplots/quadrats (relevé method), or recorded first in subplots/quadrats, and second with additional species listed outside the quadrats but inside the plot. (i) When using the relevé method, simply enter the data using a single subplot/quadrat, and note in the Sampling Design Description that the single subplot/quadrat is really a full plot. (ii) When using subplots/quadrats, record addition species otuside the quadrats but inside the plot as if they are part of an additional quadrat (its helpful to give the “quadrat” a distinct name like “QOutside” or”RestofPlot”). (The website was initially designed for Transect data only, and the current interface for Plot data contain some work-a-rounds).</li>
									</ol>
