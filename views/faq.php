								<a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapse40">
									What if I have recorded species cover by strata or growth form (e.g. White oak tree – 25% cover, White oak-sapling – 5% cover)?
								</a>
							</div>
							<div id="collapse40" class="accordion-body collapse">
								<div class="accordion-inner">
									Floristic quality metrics are based on including the species only once with a total cover value. Thus if you want to analyze your transect or plot data, you need to generate a total cover value.  There are two ways to do this.  The first is simply to keep the species record with the highest stratum cover.  The second is to use the following equation:
									<br/>
									<br/>
									\( C_i = \left[ 1 - \prod\limits_{j=1}^n (1 - \frac {\%cov\ j}{100} ) \right] \times 100 \)
									<br/>
									<br/>
									where \( C_i \) is the percent cover of stratum \( i \) for species or growth form \( j \) in stratum \( i \) and \( \prod = \) product. 
									<br/>
									<br/>
									In the example, where White oak tree - 25% cover, White oak-sapling - 5% cover, the total cover of white oak = 28%. Please see: Jennings, M.D., D. Faber-Langendoen, O.L. Loucks, R.K. Peet, and D. Roberts.  2009.  Standards for associations and alliances of the U.S. National Vegetation Classification.  Ecological Monographs 79: 173–199.									
								</div>
							</div>
